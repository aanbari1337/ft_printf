#include "ft_printf.h"

void	ft_display_x2(unsigned int p)
{
	if (p >= 16)
		ft_display_x2(p / 16);
	p = p % 16;
	p += p < 10 ? '0' : 'A' - 10;
	write(1, &p, 1);
}
