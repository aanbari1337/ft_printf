#include "ft_printf.h"

int	n_to_hex(unsigned int p, t_tab *tab)
{
	if (tab->treat[3] == 'x')
		ft_display_x1(p);
	if (tab->treat[3] == 'X')
		ft_display_x2(p);
	return (0);
}
