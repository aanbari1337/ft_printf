#include "ft_printf.h"

int	ft_space(int n)
{
	int count;

	count = 0;
	if (n <= 0)
		return (0);
	else
	{
		while (n > 0)
		{
			write(1, " ", 1);
			n--;
			count++;
		}
	}
	return (count);
}
